export const REGIONS = ['전체', '아시아', '동남아', '유럽', '유럽/중동', '미주', '오세아니아'] as const;

export function getCountryByCode(code: string): Country | undefined {
  return COUNTRIES.find(c => c.code === code);
}

export function calculateAdjustedCost(
  country: Country,
  style: 'budget' | 'standard' | 'luxury',
  duration: number,
  currentKrwPerUsd: number
): number {
  const baseCost = country.costs[style].avg;
  const defaultDuration = country.defaultDuration;
  const scaledCost = baseCost * (duration / defaultDuration);

  const latestRate = country.historicalRates[country.historicalRates.length - 1];
  const ratioChange = currentKrwPerUsd / latestRate.krwPerUsd;

  const flightCost = country.costs[style].breakdown.flight;
  const nonFlightCost = scaledCost - flightCost * (duration / defaultDuration);

  return Math.round(flightCost + nonFlightCost * ratioChange);
}
